#include "libft.h"

int	ft_nbrlen(int nb)
{
	if (nb == -2147483648)
		return (11);
	if (nb < 0)
		return (1 + ft_nbrlen(-nb));
	if (nb > 9)
		return (1 + ft_nbrlen(nb / 10));
	return (1);
}
